create table if not exists public.prompt_lab_grants (
  grant_key       text primary key,
  target_handle   text,
  user_id         uuid references public.users(id) on delete set null,
  role            text not null check (role in ('editor', 'promoter')),
  status          text not null default 'pending' check (status in ('pending', 'active', 'revoked')),
  resolution_note text,
  created_at      timestamptz not null default now(),
  bound_at        timestamptz,
  unique (user_id, role)
);

-- 융디 계정은 handle unique 제약을 이용해 서버 측에서만 실제 UUID로 해소한다.
-- 계정이 아직 없으면 pending을 유지하며 계정을 만들거나 타인을 대신 결박하지 않는다.
insert into public.prompt_lab_grants
  (grant_key, target_handle, role, status, resolution_note)
values
  ('yunji-editor', '융디', 'editor', 'pending', 'handle 융디 계정 확인 대기')
on conflict (grant_key) do nothing;

do $grant$
declare
  matched uuid;
begin
  select id into matched from public.users where handle = '융디' limit 1;
  if matched is not null then
    update public.prompt_lab_grants
       set user_id = matched,
           status = 'active',
           resolution_note = 'public.users.handle=융디 서버 해소',
           bound_at = coalesce(bound_at, now())
     where grant_key = 'yunji-editor' and status = 'pending';
  end if;
end
$grant$;

-- promoter는 특정 handle을 추정하지 않고 현재 ReadingGo admin 계정 전체에 부여한다.
-- UUID는 public.users.is_admin=true 행에서만 서버 해소하며 일반 사용자 행은 선택하지 않는다.
-- 이전 초안의 handle 기반 pending grant가 있으면 제거한다(참조 테이블 없음).
delete from public.prompt_lab_grants where grant_key = 'hyu-promoter';

-- 재실행 시 기존 admin promoter를 active로 정규화한다.
update public.prompt_lab_grants g
   set target_handle = u.handle,
       status = 'active',
       resolution_note = 'public.users.is_admin=true 서버 해소',
       bound_at = coalesce(g.bound_at, now())
  from public.users u
 where g.user_id = u.id
   and g.role = 'promoter'
   and u.is_admin = true;

-- 현재 admin마다 UUID 기반 grant 하나. 신규 admin은 마이그레이션 재실행 시 추가된다.
insert into public.prompt_lab_grants
  (grant_key, target_handle, user_id, role, status, resolution_note, bound_at)
select 'admin-promoter:' || u.id::text,
       u.handle,
       u.id,
       'promoter',
       'active',
       'public.users.is_admin=true 서버 해소',
       now()
  from public.users u
 where u.is_admin = true
   and not exists (
     select 1 from public.prompt_lab_grants g
      where g.user_id = u.id and g.role = 'promoter'
   )
on conflict (grant_key) do update
  set target_handle = excluded.target_handle,
      user_id = excluded.user_id,
      status = 'active',
      resolution_note = excluded.resolution_note,
      bound_at = coalesce(public.prompt_lab_grants.bound_at, excluded.bound_at);

-- admin에서 해제된 계정은 재실행 시 admin-seeded promoter 권한도 회수한다.
update public.prompt_lab_grants g
   set status = 'revoked',
       resolution_note = 'public.users.is_admin=false 권한 회수'
 where g.grant_key like 'admin-promoter:%'
   and not exists (
     select 1 from public.users u where u.id = g.user_id and u.is_admin = true
   );

