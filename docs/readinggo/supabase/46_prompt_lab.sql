-- baseline은 생성 뒤 내용 수정·삭제 불가. Hyu의 baseline 승격은 새 baseline 행 INSERT다.
create or replace function public.prevent_prompt_lab_baseline_mutation()
returns trigger language plpgsql set search_path = public as $fn$
begin
  if old.fixture_type = 'baseline' then
    raise exception 'Prompt Lab baseline fixtures are immutable';
  end if;
  if tg_op = 'UPDATE' then new.updated_at = now(); return new; end if;
  return old;
end
$fn$;

drop trigger if exists trg_prompt_lab_baseline_immutable on public.prompt_lab_fixtures;
create trigger trg_prompt_lab_baseline_immutable
  before update or delete on public.prompt_lab_fixtures
  for each row execute function public.prevent_prompt_lab_baseline_mutation();

